public class Addition {
    public static void main(String[] args) {
        // Assign values to the two numbers
        int num1 = 5;
        int num2 = 10;

        // Add the two numbers
        int sum = num1 + num2;

        // Output the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
